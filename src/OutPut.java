/**
 * 题目描述
 * 实现字母的大小写转换。多组输入输出。
 *输入
 *  A
 * B
 * 输出
 * a
 * b
 */

import java.util.Scanner;
public class OutPut {
        public static void main(String[] args){
            Scanner sc = new Scanner(System.in);
            while(sc.hasNext()){
                String n = sc.next();
                //
                System.out.println(n.toLowerCase());
            }
        }
    }

